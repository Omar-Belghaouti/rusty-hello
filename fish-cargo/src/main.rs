fn main() {
    let n = 20;

    if n < 30 {
        println!("n is less than 30");
    } else {
        println!("n is greater than 30");
    }

    if n == 20 {
        println!("n is equal to 20");
    } else if n == 30 {
        println!("n is equal to 30");
    } else {
        println!("n is not equal to 20 or 30");
    }
}
